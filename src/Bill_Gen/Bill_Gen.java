    //function(calculate_Price) to calculate the price with return type float.
    public float calculate_Price(){ 
        float price = 0;
        int baseprice=Integer.parseInt(tbaseprice.getText());
        float roadtax=Float.parseFloat(troadtax.getText());
        float gst=Float.parseFloat(tGST.getText());
        
        roadtax=(baseprice*roadtax)/100;
        gst=(baseprice*gst)/100;
        
        price=baseprice+roadtax+gst;
        
        ttotalprice.setText(Float.toString(price));
        return price;
    }
