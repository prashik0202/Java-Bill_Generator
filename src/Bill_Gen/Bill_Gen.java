    private void bcheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcheckActionPerformed
        // TODO add your handling code here:
        String phone=new String(tphone.getText());
        String email=new String(temail.getText());
        
        Pattern check=Pattern.compile("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[ .]{1}+[a-zA-Z0-9]+$",Pattern.CASE_INSENSITIVE);
        
        if(!check.matcher(email).find()){
            JOptionPane.showMessageDialog(null,"Please enter valid emial address","ERROR",JOptionPane.ERROR_MESSAGE);
        }
        if(!Pattern.matches("[0-9]{10}$",phone)){
            JOptionPane.showMessageDialog(null,"Please enter valid phone number","ERROR",JOptionPane.ERROR_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(null,"Correect details!");
        }
    }//GEN-LAST:event_bcheckActionPerformed

    public int bill_no(){
        int max=9999;
        int min=99;
        int range=max-min+1;
        
        int rand=(int)(Math.random() * range) + min;
        
        return rand;
    }
    
